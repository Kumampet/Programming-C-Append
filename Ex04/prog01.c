#include <stdio.h>

int main(){
  int n;
  int *p;

  n = 555;
  p = &n;
  
  printf("The value of n : %d\n",n);//nに格納されている値
  printf("The address of n : %p\n",&n);//変数nが与えられているアドレス
  printf("The value pointed by p : %d\n",*p);//ポインタpが指す(変数n自体がもっている)値
  printf("The value of p : %p\n",p);//ポインタpの値=変数nのアドレス
  printf("The address of p : %p\n",&p);//ポインタp自体が与えられちるアドレス

  return 0;
}
